g=9.8;
u=input('enter the  value of plane speed ');
y= linspace(0,1000,100);
theta=input('Enter the angle of the wind to the plane (in degrees): ');
w=input('enter the value of wind speed ');
vw=w*cosd(theta);
vwsin=w*sind(theta);
alpha=atand(vwsin/(u+vw));
k=0.004;
x1=(1/k)*(log(1+(u+vw)*sqrt(k/g)*acosh(exp(k*y))));
x2=(cosd(alpha)/k)*(log(1+(u+vw)*sqrt(k/g)*acosh(exp(k*y))));
%y=(1/k)*(log(cosh(sqrt(g/k))*(1/(u+vw))*(exp(k*x)-1)));
%y1=(1/k)*(log(cosh(sqrt(g/k))*(1/(u+vw))*(exp(k*x*secd(alpha))-1)));
h1=axes;
plot(x1,y,x2,y);
set(gca,'YDir','reverse');
xlabel('Horizontal distance (in meters)');
ylabel('Vertical distance (in meters)');
legend('Without object drift','With object drift');